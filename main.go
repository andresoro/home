package main

import (
	"fmt"
	"log"
	"net/http"
	"os"
)

func determineListenAddress() (string, error) {
	port := os.Getenv("PORT")
	if port == "" {
		return "", fmt.Errorf("$PORT not set")
	}
	return ":" + port, nil
}

func main() {
	addr, err := determineListenAddress()
	if err != nil {
		log.Fatal(err)
	}

	fs := http.FileServer(http.Dir("assets"))
	http.Handle("/", fs)
	log.Println("Starting Server...")
	http.ListenAndServe(addr, nil)
}
